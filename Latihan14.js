var angka = parseInt(prompt("masukkan angka : "));

if (angka === 1) {
  alert("angka yang anda masukkan adalah 1.");
} else if (angka === 2) {
  alert("angka yang anda masukkan adalah 2.");
} else if (angka === 3) {
  alert("angka yang anda masukka adalah 3.");
} else {
  alert("angka yang anda masukkan salah!");
}
